// Camel case
// Camel case is a naming convention where the first letter of each word is capitalized except for the first word.
let firstName = "John";
let lastName = "Doe";
let fullName = firstName + " " + lastName; // Concatenation operator

// Snake case
// Snake case is a naming convention where words are separated by underscores.
let first_name = "John";
let last_name = "Doe";
let full_name = first_name + "_" + last_name; // Concatenation operator

// Pascal case
// Pascal case is a naming convention where the first letter of each word is capitalized.
let FirstName = "John";
let LastName = "Doe";
let FullName = FirstName + " " + LastName; // Concatenation operator